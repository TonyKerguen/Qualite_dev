package feature1;
public class User {
    String nom;
    String prenom;
    int ID;

    public User(String nom, String prenom, int ID) {
        this.nom=nom;
        this.prenom = prenom;
        this.ID= ID;

    }
     public String getnom(){
        return this.nom;
     }    
     public String getprenom(){
        return this.prenom;
     }
     public int getID(){
        return this.ID;
     }





}





